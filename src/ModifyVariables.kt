fun main() {
    var counter = 23
    println("Variable is $counter before modification.")

    counter += 9
    println("Variable is now: $counter")

    counter -= 9
    println("Variable is now: $counter")

    counter /= 9
    println("Variable is now: $counter")

    counter *= 9
    println("Variable is now: $counter")
}